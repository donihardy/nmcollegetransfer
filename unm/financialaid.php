            <p>The Office of Admissions along with the Office of Financial Aid administer the majority of Scholarships at the University of New Mexico. Scholarship awards and selection processes are subject to change. Scholarships are traditionally known as merit-based, and a competitive process usually accompanies the activities surrounding selection and monitoring of the various programs. Major responsibilities and duties within the scholarship structure deal with the formation of student programs and the administration of these programs.</p>
