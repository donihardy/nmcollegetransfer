            <br /><p>Who is Eligible for the Pell Grant? - To receive this grant, you must be an undergraduate student who has not earned a bachelor's or professional degree. (A professional degree is usually earned in a field such as medicine, law, or dentistry after earning a bachelor's degree.)</p>
            <br /><p>How is the amount of Pell Grant determined? - The Pell Grant amount you receive depends on your Expected Family Contribution (EFC) and your enrollment (the number of credits you take.)
            Amounts can change yearly. The maximum Federal Pell Grant for the 2015-16 award year (July 1, 2014 to June 30, 2015) is $5,775. The amount you get, though, will depend on: your financial need, (EFC), your cost of attendance, your status as a full-time or part-time student, and your plans to attend school for a full academic year or less.</p>
