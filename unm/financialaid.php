            <h4 id="loan">Loans</h4>
            <p>Loans are the last type of aid for which you are considered, after gift aid and work-study. Loans are considered self-help financial aid because you must repay the money loaned to you. You have the right to cancel, reduce, or accept in part any of the loans that have been offered or accepted in your Financial Aid package. UNM encourages responsible borrowing, so you should determine the amount you need and only borrow that amount. Your loans can be reduced or canceled even after the funds have been disbursed to your Student Account.</p>
