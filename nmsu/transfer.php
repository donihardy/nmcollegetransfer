              <p>How to request transcripts to be sent from NMSU to another institution:</p>
              <p>An official transcript is the University’s certified statement of your complete NMSU academic record, including coursework, grades, and degrees earned. Credit hours earned through transfer work are not listed in detail, but do appear as cumulative totals.</p>
              <p>Whether delivered as a digitally signed PDF or a printed copy, the official transcript includes features that ensure the authenticity and security of the document.</p>

              <ul><li>eTranscripts (Electronic Transcripts) are delivered to any valid email address via secure, certified PDF. Cost is $10.</li>
                <li>Printed Transcripts are delivered by first-class mail or UPS Next Day delivery or can be picked up at the Registrar’s Office. Cost is $12.25.</li></ul>
