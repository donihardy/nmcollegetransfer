          <p><strong>Returning VA Students:</strong><p>
            <p>Each semester, returning VA Students must submit a request for certification form to the VACO. This form is available from the VACO in the Student Services Center, or online at:http://www.roswell.enmu.edu/forms/admissions/veteran_request_for_certification.pdf</p>
            <p><strong>All VA Students:</strong></p>
            <p>You must notify VACO of ANY changes made to enrollment.
              All degree and certificate programs offered at ENMU-Roswell are approved for Veterans’ Administration educational benefits. The Veterans’ Administration (VA) WILL NOT fund courses other than courses those pertaining to the degree programs. (This does not apply to remedial courses.) It is the student’s responsibility to furnish the VACO with a degree plan at the initial certification period. The certifying official cannot certify veterans for courses taken outside the degree program.</p>
              <p>The VACO is located in the Student Services Center, which serves as the Veterans’ Affairs center at ENMU Roswell. All necessary forms for applying for these benefits are available in that office. New Mexico Army National Guardsmen must provide Form 2384, Basic Eligibility for Army National Guard/Reserve, bearing the signature of the Educational Officer from State Headquarters in Santa Fe, before any benefits will be processed.</p>
              <p>Students applying for veterans’ benefits should apply 8-10 weeks prior to the semester for which they are enrolling in order to allow the VA sufficient processing time. It is to the veteran’s benefit to meet with their advisor prior to selecting the next semester’s classes in order to make sure that the desired classes are within the veteran’s degree plan. Students will receive a certificate of eligibility from the VA and must bring this certificate to the Admissions and Records Office before your classes will be certified with the VA. Students will receive benefit checks directly from the VA and are then responsible for making payment on their tuition. Further important information about benefits, procedures and VA regulations regarding educational benefits may be obtained in the Student Services Center.</p>
              <p><strong>Tuition Payments Forgiven</strong></p>
              <p>HB 715 requires state-funded educational institutions to forgive tuition payments owed by residents of New Mexico when the student is conscripted or enters the military service on or after August 1, 1990. Also, the bill requests institutions credit students for the full amount of the payments made when students reenroll in that institution at a future date.
              </p>
