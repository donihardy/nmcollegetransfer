                <h4 id="tes">Transferable Courses</h4>
                <p>Credit is accepted for college‑level work from institutions accredited by an accrediting body recognized
                  by the Commission on Recognition of Postsecondary Accreditation (CORPA). Courses which would not
                  be accepted include, but are not limited to, some technical/occupational courses, doctrinal religion
                  courses and basic skills level courses. ENMU-Roswell does not grant credit for work experience.</p>
                  <p>Credit for college-level work from an institution that is a candidate for accreditation by a CORPA
                    recognized body will be awarded after 15 credit hours have been earned at ENMU‑Roswell with a
                    minimum grade point average of 2.0.</p>
                    <p>To further enhance articulation between all state-supported institutions in New Mexico, ENMU-Roswell
                      adopts the principle of treating transfer students as native students in the application of credit toward
                      certificate or degree requirements.</p>
                      <p>Students who wish to request a transfer credit evaluation must complete an Admission Application to
                        ENMU-Roswell, and have original official academic transcripts sent directly to the University from each
                        institution previously attended. Transfer credits are evaluated when all transcripts for that student have
                        been received. Applicants are required to declare a major for evaluation of transfer credit. Transfer
                        credits are evaluated only for students seeking a degree or certificate. Transfer grade point averages are
                        used in the calculation of grade point averages for graduation “With Honors” only. Foreign students who
                        wish to have foreign transcripts evaluated for transfer credit must provide an English translation of all
                        transcripts. The English translation must demonstrate how the courses are equivalent in grade and
                        content to those taught in the United States. Please contact the Director of Admissions and Records for
                        more information.</p>
                        <p><a href="http://www.roswell.enmu.edu/catalogs/catalog.pdf">Transfer Info</a></p>
