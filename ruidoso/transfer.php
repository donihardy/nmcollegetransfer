              <h4 id="tes">Transferable Courses</h4>
              <p>New Mexico’s colleges and universities have
                collaborated to produce guides to assist students who plan to transfer
                before completing a program of study. Course modules are designed to
                help students select courses carefully so that they may transfer with little
                or no loss of credit. However, planning for effective transfer with maximum
                efficiency is ultimately the student’s responsibility. Responsible
                transfer planning includes early and regular consultation with the
                intended degree-granting institution to assure that all pre-transfer
                coursework will meet the requirements of the desired degree.</p>
                <p>Transferable Lower-Division General Education Common Core:
                  Student enrolling for first-year study who have not yet selected either
                  an academic focus or the institution from which they want to graduate
                  are advised to take courses during the freshman year outlined in
                  the Lower Division General Education Common Core. These courses
                  are guaranteed to transfer to any other New Mexico public college or
                  university and apply toward associate and baccalaureate degree program
                  requirements. Students should consult advisors at their current
                  institutions regarding which specific courses fit these categories.</p>
                  <p>Students preparing for careers in engineering, health sciences or
                    other profession-related fields are advised that some of this course
                    work may not transfer toward general education requirements but in
                    most cases will apply toward elective requirements.</p>
                    Visit: <a href="https://issuu.com/enmuruidoso/docs/catalog_2015-2016/28">Transfer Info</a>
                    <h4 id="commoncore">Common Core Courses</h4>
                    <p>ENMU-Ruidoso has developed the following lists of courses, consistent
                      with requirements of state law (Chapter 224 of the Laws of New
                      Mexico, 1995) and Higher Education Department Rule 251 (Transfer
                      and Articulation – General Education component) to facilitate the
                      transfer of students between ENMU-Ruidoso and other accredited
                      public institutions of higher education in New Mexico. These
                      courses are known as the General Education Common Core. These
                      courses, once they are successfully completed, will transfer to any
                      accredited public institution of higher education in New Mexico and
                      apply (except as noted below) toward the bachelor’s degree program
                      requirements.</p>
                      <p>ENMU-Ruidoso advises students who have identified a particular
                        University at which they want to complete their bachelor’s degree to
                        consult the transfer guide for that institution for more detailed advice.</p>
                        <p>ENMU-Ruidoso advises students preparing for careers in engineering,
                          health sciences or other profession-related fields that some of this
                          course work may not transfer toward the general education requirements
                          but will usually apply toward elective requirements.</p>
