                                            <h4 id="dual">High School Dual Enrollment</h4>
                                            <p>The Dual Credit is a program in which qualified high school students can take college level courses and earn high school and college credit while tuition cost is waived by ENMU-Ruidoso.</p>

                                            <p>Students must meet the following eligibility requirements:</p>
                                            <ul><li>Must have at least a 2.5 cumulative GPA (grade point average)</li>
                                            <li>Student must be 16 years old or older</li>
                                            <li>Must be currently enrolled as a junior or senior in high school</li>
                                            <li>Student must complete a university placement assessment for reading, writing, and mathematics prior to enrolling in the class</li></ul>
