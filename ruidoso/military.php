              <ol type="1"><li>File a written appeal (letter with Student Services, providing information regarding the course(s) being appealed). This information includes the following: a. A course description b. Syllabus from the course at the time it was taken, and/or c. Other documentation about the content of the course being appealed. </li>
                <li>Within 30 days of the written appeal, the file will be reviewed and the student will be contacted in writing. If the appeal is denied, the letter will explain the reason for the denial. </li>
                <li>If the student is denied and wants to continue the appeal, the student must indicate this in writing to the Vice President of Academic Learning. The second appeal must be submitted within 30 days of the first appeal decision letter. </li>
