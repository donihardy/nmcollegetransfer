            <p>Military credit is accepted as elective credit, and it is the decision of the individual college how the credit may be applied to degree requirements. If the American Council on Education Guide (ACE) does not have an evaluation of a course completed, and if the course is comparable to a course offered by ENMU-Ruidoso, an individual may take a course challenging examination. For these guidelines, see “Course Challenging” of the catalog.</p>
