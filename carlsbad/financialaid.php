                    <h4 id="scholarship">Scholarships</h4>
                    <p>There are two types of scholarships for students who attend NMSU Carlsbad: institutional scholarships, awarded by NMSU Carlsbad; and non-university scholarships, awarded by outside donors.</p>
                    <p>Each year, students must complete the NMSU Carlsbad scholarship application form by March 1. This one application covers all institutional scholarships. Students are notified in late July for the upcoming academic year, which begins in August.</p>
                    <p>Students who have been awarded a scholarship by an outside organization should bring a copy of the award letter with student ID to the Financial Aid office. The donor will be notified as soon as the student meets the requirements of the scholarship.</p>
                    <p>The Financial Aid office has no control over the funds provided by the donor. However, as soon as the funds have arrived on campus, the Financial Aid office will begin the process to deposit the funds to the student’s account. Funds are applied first toward outstanding charges and any left over funds will be returned to the student in the from of a check. To avoid delays, students can sign up for direct deposit at <a href="http://my.nmsu.edu">http://my.nmsu.edu</a>.</p>
                    <p><a href="https://carlsbad.nmsu.edu/departments/financial-aid/scholarships/">NMSU Carlsbad Scholarship Info</a></p>
                    <h4 id="loan">Loans</h4>
                    <p>Financial Aid and Scholarship Services at New Mexico State University is committed to providing educational funding options to students and parents to fulfill student’s goals and dreams.</p>
                    <p>Beginning with the 2010-2011 academic school year, New Mexico State University will participate in the William D. Ford Federal Direct Loan (Direct Loan) Program. Loans made through this program are referred to as Direct Loans. Eligible students and parents borrow directly from the U.S. Department of Education at participating schools. Direct Loans include subsidized and unsubsidized Direct Stafford Loans (also known as Direct Subsidized Loans and Direct Unsubsidized Loans), Direct PLUS Loans, and Direct Consolidation Loans. You repay these loans directly to the US Department of Education.</p>
                    <p>Student loans, unlike grants and work-study, are borrowed money that must be repaid, with interest, just like car loans and home mortgages. You cannot have these loans canceled because you didn’t like the education you received, didn’t get a job in your field of study or because you’re having financial difficulty. Loans are legal obligations, so before you take out a student loan, think about the amount you’ll have to repay over the years and review your borrower rights and responsibilities.</p>

