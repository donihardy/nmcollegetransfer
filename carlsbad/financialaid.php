                <p><strong>LEAP Grant</strong><br />
                  New Mexico Leveraging Educational Assistance Parternership Grant–LEAP (formerly known as SSIG): is a combined federal and state grant awarded to undergraduate students to help with educational costs. To be eligible for LEAP, you must be a New Mexico resident with demonstrated exceptional need.</p>
